package com.momoko.es.api.youtube.list;

/**
 * The Class Maxres.
 */
public class Maxres {

    private String url;
    private Integer width;
    private Integer height;

    /**
     * Gets the url.
     *
     * @return the url
     */
    public String getUrl() {
        return this.url;
    }

    /**
     * Sets the url.
     *
     * @param url
     *            the new url
     */
    public void setUrl(final String url) {
        this.url = url;
    }

    /**
     * Gets the width.
     *
     * @return the width
     */
    public Integer getWidth() {
        return this.width;
    }

    /**
     * Sets the width.
     *
     * @param width
     *            the new width
     */
    public void setWidth(final Integer width) {
        this.width = width;
    }

    /**
     * Gets the height.
     *
     * @return the height
     */
    public Integer getHeight() {
        return this.height;
    }

    /**
     * Sets the height.
     *
     * @param height
     *            the new height
     */
    public void setHeight(final Integer height) {
        this.height = height;
    }

}
